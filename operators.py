#Comparison Operators

x=45
y=36

print(x>y)       # Greater than: True if the left operand is greater than the right
print(x<y)       # Less than: True if the left operand is less than the right
print(x==y)       # Equal to: True if both operands are equaly
print(x!=y)       # Not equal to â€“ True if operands are not equal
print(x>=y)       # Greater than or equal to True if the left operand is greater than or equal to the right
print(x<=y)       # Less than or equal to True if the left operand is less than or equal to the right
print(x is y)     # True if x is same as y
print(x is not y)  # True if x is not same as y
