#Arithmatic Operators

a=9
b=4

#To add
add=a+b

#To Subtract 
sub=a-b

#To multiply
mul=a*b

#To divide(float division)
div1=a/b

#To divide(floor division)
div2=a//b

#To raise power
pow=a**b

print(add)
print(sub)
print(mul)
print(div1)
print(div2)
print(pow)

